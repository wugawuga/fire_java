package com.wuga;

public class Cafe {
	
	private String name;
	private int tall;
	private int grande;
	private int venti;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTall() {
		return tall;
	}
	public void setTall(int tall) {
		this.tall = tall;
	}
	public int getGrande() {
		return grande;
	}
	public void setGrande(int grande) {
		this.grande = grande;
	}
	public int getVenti() {
		return venti;
	}
	public void setVenti(int venti) {
		this.venti = venti;
	}
	
	
}
